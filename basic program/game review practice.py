    tmp = input("게임 난이도를 입력하시오, 단 1~9까지 정수 형태로 제한한다.\n").strip()
    if not tmp:
        print("정확하게 입력하세요!")
    if not tmp.isnumeric():
        print("Error")
        continue
    num = int(tmp)
    if 1<= num <= 9:
        level = num
        break
    else:
        print("정확하게 입력하세요!")
        continue

